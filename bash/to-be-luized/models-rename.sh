# Extracting Pokemon name and attack
[[ $1 =~ (.+)-([0-9]+)\.gif ]]

# Attack not found
if [[ ${#BASH_REMATCH[*]} == 0 ]]; then
	[[ $1 =~ (.+)\.gif ]]
else
	attack=-${BASH_REMATCH[2]}
fi

name=${BASH_REMATCH[1]}

# Zygarde 10% form
if [[ $name == zygarde && ${attack:1} == '10' ]]; then
	alt=${sigle[${attack:1}]}
	attack=''
fi

# Mimikyu Busted form
if [[ $name == mimikyu && ${attack:1} == '2' ]]; then
	alt=${sigle[${attack:1}]}
	attack=''
fi

# When name is not found in data,
# extracting alternative form
if [[ -z ${pokedata[$name]} && $name =~ (.+)-(.+) ]]; then
	name=${BASH_REMATCH[1]}
	alt=${BASH_REMATCH[2]}

	# Dealing with kuyrem white form
	if [[ $name == kyurem && $alt == white ]]; then
		alt=B
	else
		alt=${sigle[$alt]}
	fi
fi

name=${pokedata[$name]}

if [[ "${females[*]}" =~ "$name$alt" || "$name$alt" == '025C' ]]; then
	sex=f
else
	sex=$3
fi

if [[ $name$alt == '' ]]; then
	echo "Something went wrong with file $1: can't find Pok√©mon number" | tee -a rename_err.txt
else
	echo Spr${games[$4]}$sex${variants[$2]}$name$alt$attack.gif
fi
